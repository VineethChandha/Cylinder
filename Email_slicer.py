# Project - 3 Email Slicer
# Description: Slices the mail according to the requires format
#Variable[start:end:step]
#Variable[::-1](to reverse the string)

#get user email
email = input("What is your email ID: ").strip()
#slice the user name
user_name = email[0:email.index("@")] # prints the text before @
#slice the domain
domain = email[email.index("@")+1:] #prints string after @ to end
#format
output="Your user name is {} and your domain is {}".format(user_name,domain)
#display output
print(output)
